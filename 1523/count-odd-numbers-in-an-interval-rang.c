int countOdds(int low, int high){
    //因為輸入有 10^9 所以不能用for迴圈慢慢測
    //應直接用數學算出來。幾種狀況：
    //奇-奇：頭尾都有，奇-偶/偶-奇：一邊有，偶-偶：頭尾都沒有
    //ex. 1..7: 1,3,5,7 所以 (7-1)/2＋1
    //ex. 1..6: 1,2,5 所以 (6-1)/2+1
    //ex. 2..7: 3,5,7 所以 (7-2)/2+1
    //ex. 2..6: 3,5 所以 (6-2)/2

    if(low%2==0 && high%2==0) return (high-low)/2;
    else return (high-low)/2+1;
}
