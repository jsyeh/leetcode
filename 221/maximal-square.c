int table[301][301][301];
int maximalSquare(char** matrix, int matrixSize, int* matrixColSize){
    int M = matrixSize, N = matrixColSize[0];
    //int table[M+1][M+1][N+1];//table[i][j] 表示 i,j 往左上角
    int ans = 0;
    for(int i=0; i<M; i++){
        for(int j=0; j<N; j++){
            if(matrix[i][j]=='1') {
                table[0][i][j] = 1;
                ans = 1;
            }else table[0][i][j] = 0; //原來是這裡，漏了把0設進去
        }
    }

    for(int w=1; w<=M && w<=N; w++){
        for(int i=w; i<M; i++){
            for(int j=w; j<N; j++){
                if(table[w-1][i][j]==1 && table[w-1][i-1][j]==1 
                && table[w-1][i-1][j-1]==1 && table[w-1][i][j-1]==1) {
                    table[w][i][j]=1;
                    ans = (w+1)*(w+1);
                    //printf("w:%d i:%d j:%d\n", w, i, j);
                }
                else table[w][i][j]=0;
            }
        }
    }
    return ans;
}
