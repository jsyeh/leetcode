class Solution {
    public boolean lemonadeChange(int[] bills) {
        int d5 = 0, d10 = 0, d20 = 0;
        for(int i=0; i<bills.length; i++) {
            if(bills[i]==5){
                d5++;
            } else if(bills[i]==10 && d5>0) {
                d10++;
                d5--;
            } else if(bills[i]==20 && d5>0 && d5+d10*2>=3 ) {
                d20++;
                d5--;
                if(d10>0) d10--;
                else d5-=2;
            } else {
                //System.out.println(i);
                //System.out.println(bills[i]);
                //System.out.println("d5:" + d5 + " d10:" + d10);
                return false;
            }
        }

        return true;
    }
}
